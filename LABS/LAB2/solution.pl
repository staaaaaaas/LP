%var 1
%Воронов, Павлов, Левицкий и Сахаров четыре талантливых молодых
% человека. Один из них танцор, другой художник, третий певец, четвертый
% писатель. Воронов и Левицкий сидели в зале консерватории в тот вечер,
% когда певец дебютировал в сольном концерте. Павлов и писатель вместе
% позировали художнику. Писатель написал биографическую повесть о
% Сахарове и собирается написать о Воронове. Воронов никогда не слышал о
% Левицком. Кто чем занимается?

solution(Persons) :-
    Persons = ['Воронов'-Воронов, 'Павлов'-Павлов, 'Левицкий'-Левицкий, 'Сахаров'-Сахаров],
    permutation([_-танцор, _-художник, _-певец, _-писатель], Persons), %перестановка кто есть кто
    \+ Воронов = певец, \+ Левицкий = певец, %Воронов и Левицкий не певец
    \+ Павлов = писатель, \+ Павлов = художник, %Павлов не писатель и не художник
    \+ Сахаров = писатель, \+ Воронов = писатель, %Сахаров и  Воронов не писатель
    %Павлов не писатель, Воронов не писатель, Сахаров не писатель -> Левицкий писатель
    \+ member(Воронов, [Павлов, писатель, художник]).%Воронов не может быть членом компании Павлов, писатель, художник, т.к. Левицкий писатель
%Воронову сопоставляется танцор; Павлову певец; Сахарову Художник

